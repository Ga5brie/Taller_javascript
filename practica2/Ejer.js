let dia=parseInt(prompt("Introduce un numero del 1 al 7:"))//2
switch(dia){
    case 1:
        console.log("Lunes");
        break;
    case 2:
        console.log("martes");
        break;
    case 3:
        console.log("Miercoles");
        break;
    case 4:
        console.log("jueves");
        break;
    case 5:
        console.log("viernes");
        break;
    case 6: 
        console.log("Sabado");
        break;
    case 7:
        console.log("Domingo");
        break;
    default:
        console.log("Dia de seman incorrecto")
}